GROUP BY rm.material_id, rm.material_name
HAVING SUM(w.scrap_qty) > (
SELECT SUM(w.scrap_qty)
FROM wastage w
WHERE w.reason = 'overheating')
ORDER BY wastage
LIMIT 5;















